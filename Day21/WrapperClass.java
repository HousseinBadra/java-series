package Day21;

public class WrapperClass {
    public static void main(String args[]) {
        // java provides wrapper classes for primitive types
        // using these you can benefit from additional methods and features

        Integer numBox = 1; // auto boxing
        int num = numBox; // auto unboxing

        Double dblBox = 4.5; // auto boxing
        double db = dblBox; // auto unboxing

        return;
    }
}
