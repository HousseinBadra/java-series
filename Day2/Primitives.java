package Day2;
public class Primitives {
    @SuppressWarnings("unused")
    public static void main(String args[]){

        // Numbers

        long bigNumber = 12345678; // 8 bytes
        int normalNumber = 1234; // 4 bytes
        short smallNumber = 123; // 2 bytes
        byte tinyNumber = 12; // 1 byte
        
        float floatingNumber = 1.5f; // 4bytes
        double doubleNumber = 1.5; // 8 bytes

        // booleans

        boolean trueBoolean = true; // 1 byte
        boolean falseBoolean = false; // 1 byte

        // characters

        char character = 'r'; // 2 bytes
    
        return;
    }
}
